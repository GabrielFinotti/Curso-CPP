#include "Matematica.h"
#include <cmath>

int soma(int numb1, int numb2)
{
	return numb1 + numb2;
}

int sub(int numb1, int numb2)
{
	return numb1 - numb2;
}

int multi(int numb1, int numb2)
{
	return numb1 * numb2;
}

float divs(int numb1, int numb2)
{
	return numb1 / numb2;
}

int pot(int numb1, int numb2)
{
	return pow(numb1, numb2);
}

double raiz(int numb)
{
	return sqrt(numb);
}
